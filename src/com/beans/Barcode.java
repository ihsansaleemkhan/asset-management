package com.beans;

public class Barcode {
	
	private int id;
	private int ink_id;
	private String barcode;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getInk_id() {
		return ink_id;
	}
	public void setInk_id(int ink_id) {
		this.ink_id = ink_id;
	}
	public String getBarcode() {
		return barcode;
	}
	public void setBarcode(String barcode) {
		this.barcode = barcode;
	}

}
